    // handles the delete booking button click
    private void deleteBookingActionPerformed(java.awt.event.ActionEvent evt) {
        // TODO add your handling code here:
        int viewRow = UserTable.getSelectedRow();
        if (viewRow < 0) {
            JOptionPane.showMessageDialog(this, "Select a booking first");
            return;
        }

        int modelRow = UserTable.convertRowIndexToModel(viewRow);
        int bookingId = Integer.parseInt(UserTable.getModel().getValueAt(modelRow, 0).toString());

        int choice = JOptionPane.showConfirmDialog(
                this,
                "Please confirm deletion of booking ID: " + bookingId + "?",
                "Confirm Delete",
                JOptionPane.YES_NO_OPTION
        );
        if (choice != JOptionPane.YES_OPTION) {
            return;
        }

// Fixed delete logic
        try {
            BookingsDB bookingsDb = new BookingsDB(new DBManager());

            // Debug: Check if booking exists first
            System.out.println("Trying to delete booking ID: " + bookingId);

            boolean deleted = bookingsDb.deleteBooking(bookingId);

            if (deleted) {
                DefaultTableModel model = (DefaultTableModel) UserTable.getModel();
                model.removeRow(modelRow);
                JOptionPane.showMessageDialog(this, "Booking deleted successfully");
            } else {
                JOptionPane.showMessageDialog(this, "Failed to delete booking - booking not found");
            }

        } catch (Exception ex) { // Changed from Error to Exception
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error deleting booking: " + ex.getMessage());
        }
    }//GEN-LAST:event_deleteBookingActionPerformed

    // handles the view bookings in detail button click
    private void viewBookingsbtn2ActionPerformed(java.awt.event.ActionEvent evt) {
        // TODO add your handling code here:
        int viewRow = UserTable.getSelectedRow();
        if (viewRow < 0) {
            JOptionPane.showMessageDialog(this,
                    "Select a booking to view in detail");
            return;
        }

        int modelRow = UserTable.convertRowIndexToModel(viewRow);

        String bookingIdStr = UserTable.getModel()
                .getValueAt(modelRow, 0)
                .toString();

        try {

            BookingsDB bookingsDb = new BookingsDB(new DBManager());
            int bookingId = Integer.parseInt(bookingIdStr);
            Booking booking = bookingsDb.getBookingById(bookingId);

            if (booking == null) {
                JOptionPane.showMessageDialog(this, "Booking not found");
                return;
            }

            // Display the booking details
            ReciptPanelAdminView receipt = new ReciptPanelAdminView();
            receipt.displayBooking(booking);
            receipt.setVisible(true);

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Invalid booking ID format");
        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this,
                    "Error loading booking details: " + ex.getMessage());
        }
    }//GEN-LAST:event_viewBookingsbtn2ActionPerformed

    // handles the make admin button click
    private void makeAdminActionPerformed(java.awt.event.ActionEvent evt) {
        int selectedRow = UserTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Select a user first");
            return;
        }
        
        int modelRow = UserTable.convertRowIndexToModel(selectedRow);
        String userEmail = UserTable.getModel().getValueAt(modelRow, 1).toString(); 
        String userName = UserTable.getModel().getValueAt(modelRow, 0).toString(); 
        
        int choice = JOptionPane.showConfirmDialog(
            this,
            "Promote " + userName + " (" + userEmail + ") to Admin?",
            "Confirm Promotion",
            JOptionPane.YES_NO_OPTION
        );
        
        if (choice != JOptionPane.YES_OPTION) {
            return;
        }
        UsersDB userDb = new UsersDB(new DBManager()); // assuming you have a UserDB class
        boolean promoted = userDb.updateUserRole(userEmail,"ADMIN");
        if (promoted) {
            DefaultTableModel model = (DefaultTableModel) UserTable.getModel();
            model.setValueAt("ADMIN", modelRow, 2);
            JOptionPane.showMessageDialog(this, 
                    userName + " has been promoted to Admin successfully! ðŸŽ‰");
        } else {
            JOptionPane.showMessageDialog(this, 
                    "Failed to promote user - user not found");
        }
    }//GEN-LAST:event_makeAdminActionPerformed

